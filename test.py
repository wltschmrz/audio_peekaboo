def plot_mel(mel, config, title="Mel Spectrogram"):
    """AudioLDM에서 사용하는 mel spectrogram을 직접 시각화합니다.
    
    Args:
        mel: shape (C, T) 또는 (1, C, T)의 mel spectrogram
        config: AudioLDM config
        title: 그래프 제목
    """
    plt.figure(figsize=(10, 10))
    
    # (1, C, T) -> (C, T) 변환
    if len(mel.shape) == 3:
        mel = mel.squeeze(0)
    
    # numpy로 변환
    if torch.is_tensor(mel):
        mel = mel.cpu().numpy()
    
    # 시각화
    librosa.display.specshow(
        mel,
        y_axis='mel',
        x_axis='time',
        sr=16000,
        hop_length=1024,
        cmap='viridis'
    )
    
    plt.colorbar(format='%+2.0f dB')
    plt.title(title)
    plt.tight_layout()
    plt.show()
plot_mel(mel, config, title="Preprocessed Mel Spectrogram")
